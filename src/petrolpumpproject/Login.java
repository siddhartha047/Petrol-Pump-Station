    public boolean checkMatch(String username, String pass, String fileName){
        try {
            Scanner in=new Scanner(new File(fileName));
            while(in.hasNext()){
                String line=in.nextLine();
                String []info=line.trim().split(" ");
                if(info[0].equals(username)&&info[1].equals(pass))return true;
            }
            
            return false;
            
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
            return false;
        }               
    }
    
