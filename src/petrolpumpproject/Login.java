    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username=jTextField1.getText();
        String password=new String(jPasswordField1.getPassword());
        String type=(String)jComboBox1.getSelectedItem();
        
        System.out.println(username+"\t"+password+"\t"+type);
        
        if(type.equals("Employee")){
            
            if(checkMatch(username, password, "Files/Employee.txt")){
                System.out.println("Employee ok");
                EmployeeHome empHOme=new EmployeeHome(this);
                empHOme.setVisible(true);
                this.setVisible(false);
            }
            else{
                System.out.println("Emp Failed");
                JOptionPane.showMessageDialog(null, "Username Password Didn't Match");
            }
        }
        else if(type.equals("Admin")){
            
            if(checkMatch(username, password, "Files/AdminInfo.txt")){
                System.out.println("Admin ok");
                AdminHome admin=new AdminHome(this);
                admin.setVisible(true);
                this.setVisible(false);                
            }
            else{
                System.out.println("Emp Failed");
                JOptionPane.showMessageDialog(null, "Username Password Didn't Match");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

